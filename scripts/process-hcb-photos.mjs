/**
 * Process Hatz Court Builders source photos → public/images/hcb/*.jpg (high quality, SEO filenames).
 * Run: node scripts/process-hcb-photos.mjs
 */
import fs from "node:fs";
import path from "node:path";
import sharp from "sharp";

const SOURCE_DIR = path.resolve("c:/Users/Lenovo/Downloads/HCB Photos");
const OUT_DIR = path.resolve("public/images/hcb");
const SUFFIX = "Boise ID - Hatz Court Builders";
const MAX_BYTES = 3 * 1024 * 1024;
const MAX_WIDTH = 4000;
const JPEG_QUALITY = 92;

function slugify(text) {
  return text
    .toLowerCase()
    .replace(/['']/g, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function normalizeDashes(text) {
  return text.replace(/[\u2010-\u2015\u2212]/g, "-");
}

function extractDescriptionFromFilename(name) {
  const normalizedName = normalizeDashes(name);
  const base = normalizeDashes(path.parse(normalizedName).name);
  const suffixPatterns = [
    /\s*-\s*Boise ID\s*-\s*Hatz Court Builders\s*$/i,
    /\s*-\s*Boise, Idaho\s*-\s*Hatz Court Builders\s*$/i,
  ];
  let desc = base;
  for (const pattern of suffixPatterns) {
    desc = desc.replace(pattern, "").trim();
  }
  return desc || base;
}

function outputFilename(description) {
  return `${slugify(description)}-${slugify(SUFFIX)}.jpg`;
}

async function encodeJpeg(sharpInstance) {
  const meta = await sharpInstance.metadata();
  let pipeline = sharpInstance.clone().rotate();

  if (meta.width && meta.width > MAX_WIDTH) {
    pipeline = pipeline.resize({ width: MAX_WIDTH, withoutEnlargement: true });
  }

  let quality = JPEG_QUALITY;
  let buffer = await pipeline.jpeg({ quality, mozjpeg: true }).toBuffer();

  for (let pass = 0; buffer.length > MAX_BYTES && pass < 12; pass++) {
    quality = Math.max(80, quality - 2);
    buffer = await pipeline.jpeg({ quality, mozjpeg: true }).toBuffer();
  }

  if (buffer.length > MAX_BYTES) {
    throw new Error(`Could not compress below 3MB (got ${buffer.length} bytes)`);
  }

  return buffer;
}

function removeLegacyOutputs() {
  for (const file of fs.readdirSync(OUT_DIR)) {
    if (/\.webp$/i.test(file)) {
      fs.unlinkSync(path.join(OUT_DIR, file));
    }
  }
}

async function main() {
  fs.mkdirSync(OUT_DIR, { recursive: true });
  removeLegacyOutputs();

  const sources = fs.readdirSync(SOURCE_DIR).filter((f) => /\.(jpe?g|png|heic|webp)$/i.test(f));
  const manifest = [];

  for (const sourceName of sources.sort()) {
    const description = extractDescriptionFromFilename(sourceName);
    const outName = outputFilename(description);
    const outPath = path.join(OUT_DIR, outName);
    const srcPath = path.join(SOURCE_DIR, sourceName);

    try {
      const input = sharp(srcPath, { failOn: "none" }).rotate();
      const buffer = await encodeJpeg(input);
      fs.writeFileSync(outPath, buffer);

      const webPath = `/images/hcb/${outName}`;
      manifest.push({ sourceName, description, webPath, bytes: buffer.length });
      console.log(`${(buffer.length / 1024).toFixed(0)}KB\t${outName}`);
    } catch (err) {
      console.warn(`SKIP\t${sourceName}\t${err instanceof Error ? err.message : err}`);
    }
  }

  manifest.sort((a, b) => a.webPath.localeCompare(b.webPath));
  const paths = manifest.map((m) => m.webPath);

  const tsContent = `/** Auto-generated by scripts/process-hcb-photos.mjs — do not edit by hand. */
export const HCB_SEO_FILENAME_SUFFIX = ${JSON.stringify(SUFFIX)} as const;

export const HCB_GALLERY_IMAGE_PATHS = [
${paths.map((p) => `  ${JSON.stringify(p)},`).join("\n")}
] as const;

export type HcbGalleryImagePath = (typeof HCB_GALLERY_IMAGE_PATHS)[number];

export function hcbImageAlt(imagePath: string): string {
  const filename = imagePath.split("/").pop() ?? "";
  const stem = filename.replace(/\\.(webp|jpe?g|png)$/i, "");
  const suf = \`-\${slugifySuffix(HCB_SEO_FILENAME_SUFFIX)}\`;
  const i = stem.endsWith(suf) ? stem.length - suf.length : -1;
  const kw = i === -1 ? stem : stem.slice(0, i);
  return kw.replace(/-/g, " ").trim();
}

function slugifySuffix(text: string): string {
  return text.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-+|-+$/g, "");
}
`;

  fs.writeFileSync(path.resolve("src/lib/hcb-gallery-images.ts"), tsContent);

  const compatContent = `/** Re-exports HCB gallery paths for legacy imports. */
export {
  HCB_SEO_FILENAME_SUFFIX as GPM_SEO_FILENAME_SUFFIX,
  HCB_GALLERY_IMAGE_PATHS as GPM_GALLERY_IMAGE_PATHS,
  hcbImageAlt as gpmImageAlt,
  type HcbGalleryImagePath as GpmGalleryImagePath,
} from "@/lib/hcb-gallery-images";
`;

  fs.writeFileSync(path.resolve("src/lib/gpm-gallery-images.ts"), compatContent);

  fs.writeFileSync(
    path.resolve("scripts/hcb-photos-manifest.json"),
    JSON.stringify(manifest, null, 2),
  );
  console.log(`\nWrote ${paths.length} images → ${OUT_DIR}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
